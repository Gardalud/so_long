#include "libft.h"

int	ft_i(int i)
{
	int		x;
	char	*str;

	str = ft_itoa(i);
	if (!str)
		return (0);
	ft_putstr_fd(str, 1);
	x = ft_strlen(str);
	free(str);
	return (x);
}

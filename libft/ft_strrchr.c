#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*stri;
	int		len;

	stri = (char *)str;
	c = (char)c;
	len = ft_strlen(str);
	while (len >= 0)
	{
		if (stri[len] == c)
			return (&stri[len]);
		len--;
	}
	return (0);
}
